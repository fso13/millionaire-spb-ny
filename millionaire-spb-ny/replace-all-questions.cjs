// Функция для получения вопроса из базы данных
function getQuestion(category, difficulty, points, questionNum) {
  const categoryData = questionsDatabase[category];
  if (!categoryData) return null;
  
  const difficultyData = categoryData[difficulty];
  if (!difficultyData) return null;
  
  const pointsData = difficultyData[points];
  if (!pointsData || pointsData.length === 0) return null;
  
  // Используем questionNum - 2, так как вопросы начинаются с номера 2 (первый уже есть)
  const index = (questionNum - 2) % pointsData.length;
  return pointsData[index];
}

// Читаем файл
const filePath = path.join(__dirname, 'src', 'data', 'yourGameQuestionsRegular.json');
const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));

// Функция для замены вопросов
function replaceQuestions(data) {
  let replacedCount = 0;
  
  // Проходим по всем уровням сложности
  ['easy', 'medium', 'hard'].forEach(difficulty => {
    if (!data[difficulty] || !data[difficulty].themes) return;
    
    // Проходим по всем темам
    data[difficulty].themes.forEach(theme => {
      const categoryName = theme.name;
      
      // Пропускаем "Фигурное катание", так как оно уже заменено
      if (categoryName === 'Фигурное катание') return;
      
      // Проходим по всем вопросам
      theme.questions.forEach((question) => {
        // Проверяем, является ли это placeholder-вопросом
        if (question.question && question.question.includes(' - вопрос')) {
          const match = question.question.match(/(.+?) - вопрос (\d+)/);
          if (match) {
            const questionCategory = match[1];
            const questionNum = parseInt(match[2]);
            const points = question.points;
            
            // Получаем вопрос из базы данных
            const replacement = getQuestion(questionCategory, difficulty, points, questionNum);
            
            if (replacement) {
              question.question = replacement.q;
              question.answer = replacement.a;
              replacedCount++;
            }
          }
        }
      });
    });
  });
  
  return replacedCount;
}

